export function loadChinaIfNeeded(node, resolve) {
  const val = node && node.data ? node.data.value : null
  const parentVal = node && node.parent && node.parent.data ? node.parent.data.value : null
  if (val === '中国') {
    resolve(chinaProvinces.map(p => ({ label: p, value: p, leaf: !chinaCitiesMap[p] })))
    return
  }
  if (parentVal === '中国' && chinaProvinces.includes(val)) {
    const cities = chinaCitiesMap[val] || []
    resolve(cities.map(c => ({ label: c, value: c, leaf: true })))
    return
  }
  resolve([])
}

